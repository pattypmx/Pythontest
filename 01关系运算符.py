# ==	检查两个操作数的值是否相等，如果是，则条件为真。	a=1,b=1，则（a==b）为true
# ！=检查两个操作数的值是否相等，如果不等，则条件为真。	a=1,b=2，则（a！=b）为true
# >	检查左操作数的值是否大于右操作数，如果是，则条件成立。	a=4,b=1，则（a>b）为true
# <	检查左操作数的值是否小于右操作数，如果是，则条件成立。	a=4,b=5，则（a<b）为true
# >=	检查左操作数的值是否大于等于右操作数，如果是，则条件成立。a=4,b=4，则（a>=b）为true
# <=检查左操作数的值是否小于等于右操作数，如果是，则条件成立。a=4,b=4，则（a<=b）为true

# 定义两个值
